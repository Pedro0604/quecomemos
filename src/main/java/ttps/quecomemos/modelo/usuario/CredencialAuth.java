package ttps.quecomemos.modelo.usuario;

public class CredencialAuth {

    private int dni;
    private String clave;

    // Getters y Setters
    public int getDni() {
        return dni;
    }

    public void setDni(int dni) {
        this.dni = dni;
    }

    public String getClave() {
        return clave;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }

}
